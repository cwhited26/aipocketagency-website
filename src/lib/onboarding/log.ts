// lib/onboarding/log.ts — the structured logger for the vertical-onboarding seeder. One JSON
// line per event, scoped "onboarding", so vertical picks / persona seeds are greppable in the
// platform logs. No console.log — every diagnostic goes through here (mirrors lib/launch-kit/log.ts).

type Level = "info" | "warn" | "error";
type Fields = Record<string, unknown>;

function emit(level: Level, msg: string, fields?: Fields): void {
  const line = JSON.stringify({
    ts: new Date().toISOString(),
    scope: "onboarding",
    level,
    msg,
    ...(fields ?? {}),
  });
  if (level === "error") console.error(line);
  else if (level === "warn") console.warn(line);
  else console.info(line);
}

export const onboardingLog = {
  info: (msg: string, fields?: Fields): void => emit("info", msg, fields),
  warn: (msg: string, fields?: Fields): void => emit("warn", msg, fields),
  error: (msg: string, fields?: Fields): void => emit("error", msg, fields),
};
